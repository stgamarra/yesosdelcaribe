        <div class="col-sm-6 col-md-6 text-justify" style="font-family: 'helvetica_condensed'; font-size: 1.1em; padding-left:30px;">
        <p>• Los productos que tienen como base el yeso, no son adecuados para ubicarse en exteriores, como tampoco para estar sujetos a la acción de los elementos atmosféricos o sometidos a humedad excesiva.<br><br>
            
• Cuando el saco de yeso se almacena en ambiente seco (bajo estibas y no directamente en el suelo), este producto tendrá una vida media de uso seis meses desde la fecha de fabricación que se indica en cada saco.<br><br>
            
• La absorción de humedad puede producir cambios en las propiedades físicas, tales como reducción en la resistencia de los yesos o prolongación del tiempo de fraguado. Para salvaguardar la calidad del producto durante su empleo, los sacos abiertos o parcialmente usados deben ser doblados y cerrados cuidadosamente.<br><br>
            
• Cada saco lleva impresa la fecha. Deben rotarse las existencias de manera que el material mas viejo sea utilizado primero.<br><br>

            • Para la preparación de la mezcla se recomienda:<br><br>
            
1) Pese el yeso y el agua para cada mezcla.  La proporción agua – yeso es un factor crítico. Nuestra fabrica recomienda la siguiente proporción: 0.80 KILOS DE AGUA: 1 KILO DE YESO (Ejemplo para placas: 5 KILOS DE YESO: 4 KILOS DE AGUA);<br><br>

2) Use agua potable a temperaturas entre 21ºC y 38ºC.  Las variaciones en la temperatura de la mezcla (agua y yeso) pueden producir cambios en el tiempo de fraguado;<br><br>

3) Es importante mantener el agua y el yeso en un ambiente con temperatura dentro del rango mencionado antes de usarlo.  A más alta temperatura del agua, más corto será el tiempo de fraguado;<br><br>

4) Vacíe o espolvoree despacio y suavemente el yeso en el agua, sin que queden montones o grumos.  Permítale remojarse o humedecerse por un minuto.  El yeso debe estar totalmente esparcido en el agua antes de mezclarlo.  Es importante mantener la proporción de agua y yeso; si la masa queda clara es decir, que tiene más agua de la necesaria, el yeso coagulara con lentitud y no adquirirá solidez; si tiene poca agua endurecerá pronto y será difícil su manejo.<br><br>
</p>
        
