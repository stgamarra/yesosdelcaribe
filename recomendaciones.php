    <div class="col-sm-6 col-md-6 text-justify" style="font-family: 'helvetica_condensed'; font-size: 1.1em; padding-left:30px;">
        <p style="font-family: 'helvetica_boldcondensed'; font-size: 1.1em;">NOTA: Las proporciones citadas de yeso y agua son las utilizadas en los métodos estándar de prueba de laboratorio, y no son necesariamente las que se utilizan en la práctica. La fluidez precisa de empleo se ajustará según las necesidades de cada aplicación individual.<br><br></p> 

<p>Para el mezclado del yeso y el agua, se recomienda:<br>
1) Mezclar manualmente, mecánicamente o con equipo de mezclado continuo;<br><br>

2) El mezclado manual generalmente es aceptable para cuando se preparan cantidades menores a 25 kg de yeso;<br><br>

3) Tiempos largos de mezcla, aumentan la resistencia del vaciado y acortan el tiempo de fraguado;<br><br>

4) El amasado manual se puede realizar con las propias manos protegidas con guantes, con la paleta o con otro utensilio, removiéndolo enérgicamente hasta que la pasta tome un aspecto homogéneo y sin grumos;<br><br>

5) Antes de un nuevo amasado hay que eliminar del recipiente, así como de todas las herramientas, los restos de yeso endurecido sobrantes del proceso anterior, puesto que el yeso ya fraguado actúa como acelerante sobre el fraguado de la nueva pasta que se va a preparar.<br><br>

El yeso se debe mezclar en pequeñas cantidades y siempre deberemos aplicarlo rápidamente o la mezcla se endurecerá. Nunca se deben aprovechar los restos de una mezcla que ya está endureciendo para añadirlos a una nueva mezcla pues se obtendrá lo que se conoce como “yeso muerto”, de baja resistencia y poca adherencia, que acabará desprendiéndose con facilidad.<br>
</p>
        
